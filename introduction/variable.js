// five things you need to declare a variable
/*
    1.  keyword -> ex. "var"
    2.  variable name -> ex. "price"
    3.  assignment operator -> ex. "=" 
    4.  value -> ex. "13"
    5. semicolon -> ex. ";"
*/

// numeric - can be written with or without quote
var price = 13;
var age = "17";
console.log(price);

// string - should be written into quote
var name = "sujat";
var location = "dhaka";
console.log(location);

// boolean - take the true or false value
var isSerious = true;
var isSingle = false;
console.log(isSerious);
