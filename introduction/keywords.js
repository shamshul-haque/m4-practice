// naming rules
/*
    1. variable name can not be any keywords -> ex. (var false = 96;)
    2. variable has to be in one word and have to contain no space -> ex. (var my home = dhaka;)
    3. variable name can not have quotation -> ex. (var "name" = "Sujat";)
    4. variable name can not starts with number -> ex. (var 99road = "nazimussin";)
    5. Can not use dash "-" -> ex. (var user-name = "bappa";)
    6. Can be written in camel-case -> ex. (var userName = "bappa";)
*/
