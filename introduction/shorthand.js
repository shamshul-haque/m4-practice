// variable declaration
var tomatoPrice = 25;
console.log(tomatoPrice);

// declared variable value updated
tomatoPrice = 30;
console.log(tomatoPrice);

// add some value with existing value
tomatoPrice = tomatoPrice + 10;
console.log(tomatoPrice);

// shorthand
tomatoPrice += 20;
console.log(tomatoPrice);
tomatoPrice -= 5;
console.log(tomatoPrice);
tomatoPrice *= 10;
console.log(tomatoPrice);
tomatoPrice /= 15;
console.log(tomatoPrice);

var a = 50;
// increment
a++;
console.log(a);
// decrement
a--;
console.log(a);
