// simple comparison
console.log(5 < 6); //less
console.log(5 > 6); //greater
console.log(5 == 6); //equal
console.log(5 != 6); //not equal
console.log(5 <= 6); //less or equal
console.log(5 >= 6); //greater or equal

// multiple comparison
a > a2 && b1 == b2 && c1 < c2; //for and "&&" operator to work all condition have to be fulfil
a > a2 || b1 == b2 || c1 < c2; //for and "||" operator to work any one condition have to be fulfil
