var color = "red";
switch (color) {
  case "green":
    console.log("It is green");
    break;
  case "blue":
    console.log("It is blue");
    break;
  case "red":
    console.log("It is red");
    break;
  case "yellow":
    console.log("It is yellow");
    break;
  default:
    console.log("It is nothing");
}
